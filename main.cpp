#include<iostream>

int main(){

   int num1 = 8;
   int num2 = 2;
   int soma, subt, mult, div;
   
   soma = num1 + num2;
   subt = num1 - num2;
   mult = num1 * num2;
   div = num1 / num2;


   std::cout<<"8 + 2 = "<<soma<<std::endl;
   std::cout<<"8 - 2 = "<<subt<<std::endl;
   std::cout<<"8 * 2 = "<<mult<<std::endl;
   std::cout<<"8 / 2 = "<<div<<std::endl;



    return 0;
}

